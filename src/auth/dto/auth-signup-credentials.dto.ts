import { IsString, Matches, MaxLength, MinLength } from 'class-validator';

export class AuthCredentialsDto {

  @IsString()
  @MaxLength(20)
  companyname?: string;

  
  @IsString()
  @MaxLength(20)
  codenumber?: string;

  @IsString()
  @MinLength(5)
  @MaxLength(15)
  phonenumber?: string;

  @IsString()
  @MaxLength(20)
  country?: string;

  @IsString()
  @MaxLength(20)
  region?: string;

  @IsString()
  @MaxLength(20)
  street?: string;

  @IsString()
  @MaxLength(20)
  address?: string;

  @IsString()
  @Matches(/(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/, {
    message: 'must be a valid email',
  })
  email?: string;
  
  @IsString()
  @MaxLength(20)
  firstname?: string;

  @IsString()
  @MaxLength(20)
  lastname?: string;

  @IsString()
  @MinLength(6)
  @MaxLength(20)
  @Matches(/((?=.*\d)|(?=.*\W+))(?![.\n])(?=.*[A-Z])(?=.*[a-z]).*$/, {
    message: 'password is too weak',
  })
  password?: string;
}

export class AuthSignInCredentialsDto {

  @IsString()
  @Matches(/(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/, {
    message: 'must be a valid email',
  })
  email?: string;

  @IsString()
  password?: string;
}

